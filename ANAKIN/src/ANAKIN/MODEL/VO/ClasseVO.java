package ANAKIN.MODEL.VO;

public class ClasseVO {
	private String Tipo_Classe, Talento_Padrao, Talento_Especial;
	private int ID_Classe;

	public String getTipo_Classe() {
		return Tipo_Classe;
	}

	public void setTipo_Classe(String tipo_Classe) {
		Tipo_Classe = tipo_Classe;
	}

	public String getTalento_Padrao() {
		return Talento_Padrao;
	}

	public void setTalento_Padrao(String talento_Padrao) {
		Talento_Padrao = talento_Padrao;
	}

	public String getTalento_Especial() {
		return Talento_Especial;
	}

	public void setTalento_Especial(String talento_Especial) {
		Talento_Especial = talento_Especial;
	}

	public int getID_Classe() {
		return ID_Classe;
	}

	public void setID_Classe(int iD_Classe) {
		ID_Classe = iD_Classe;
	}

}
